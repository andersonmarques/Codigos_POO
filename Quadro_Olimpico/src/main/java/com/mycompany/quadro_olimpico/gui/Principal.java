        Pais p = new Pais(nome);
        p.setQuantMedalhasOuro(Integer.parseInt(quantOuroSpinner.getValue().toString()));
        p.setQuantMedalhasPrata(Integer.parseInt(quantoPrataSpinner.getValue().toString()));
        p.setQuantMedalhasBronze(Integer.parseInt(quantBronzeSpinner.getValue().toString()));
        
        System.out.println(p.getNome()+"\t"+
                p.getQuantMedalhasOuro()+"\t"+
                p.getQuantMedalhasPrata()+"\t"+
                p.getQuantMedalhasBronze());
