    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        String nome = jTextFieldNome.getText();
        
        Pais p = new Pais(nome);
        p.setQuantMedalhaOuro(Integer.parseInt(jSpinnerOuro.getValue().toString()));
        p.setQuantMedalhaPrata(Integer.parseInt(jSpinnerPrata.getValue().toString()));
        p.setQuantMedalhaBronze(Integer.parseInt(jSpinnerBronze.getValue().toString()));
                
        for (int i = 0; i < 4; i++) {
            if (vPais[i] == null){
                vPais[i] = p;
                break;
            }
        }
        String saida = "";
        for (Pais v : vPais) {
            if (v != null){
            saida += v.getNome()+"\t"+
                v.getQuantMedalhaOuro()+"\t"+
                v.getQuantMedalhaPrata()+"\t"+
                v.getQuantMedalhaBronze()+"\n";
            }
        }
        jTextAreaSaida.setText(saida);
    }//GEN-LAST:event_jButtonSalvarActionPerformed

